    private void tf_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tf_cariActionPerformed
        // TODO add your handling code here:
        Object[]baris = {"ID User","Nama","No_HP","Alamat"};
        tabmode = new DefaultTableModel(null, baris);
        tabeloutz.setModel(tabmode);
        String sql_select = "select * from user where ID_User like '%"+tf_cari.getText()+"%'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql_select);
            while(hasil.next()){
                String a = hasil.getString("ID_User");
                String b = hasil.getString("Nama");
                String c = hasil.getString("No_HP");
                String salamat = hasil.getString("Alamat");
                String[] data={a,b,c,salamat};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tf_cariActionPerformed

